    const rect = formElement.getBoundingClientRect();
    const x = e.clientX - rect.left; // X –ø–æ–∑–∏—Ü–∏—è –≤–Ω—É—Ç—Ä–∏ —ç–ª–µ–º–µ–Ω—Ç–∞
    const y = e.clientY - rect.top;  // Y –ø–æ–∑–∏—Ü–∏—è –≤–Ω—É—Ç—Ä–∏ —ç–ª–µ–º–µ–Ω—Ç–∞

    // –†–∞—Å—Å—á–∏—Ç—ã–≤–∞–µ–º —É–≥–ª—ã –Ω–∞–∫–ª–æ–Ω–∞ (—É–º–µ–Ω—å—à–µ–Ω—ã –¥–ª—è –±–æ–ª–µ–µ —Ç–æ–Ω–∫–æ–≥–æ —ç—Ñ—Ñ–µ–∫—Ç–∞)
    const tiltX = (y / rect.height - 0.5) * 2;
    const tiltY = (0.5 - x / rect.width) * 2;

    // –ü—Ä–∏–º–µ–Ω—è–µ–º –Ω–∞–∫–ª–æ–Ω –∫ —Ñ–æ—Ä–º–µ
    anime({
      targets: formElement,
      rotateX: tiltX,
      rotateY: tiltY,
      duration: 300,
      easing: 'easeOutQuad'
    });
  };

  const handleMouseLeave = () => {
    // –í–æ–∑–≤—Ä–∞—â–∞–µ–º —Ñ–æ—Ä–º—É –≤ –∏—Å—Ö–æ–¥–Ω–æ–µ –ø–æ–ª–æ–∂–µ–Ω–∏–µ
    anime({
      targets: formElement,
      rotateX: 0,
      rotateY: 0,
      duration: 400,
      easing: 'easeOutQuad'
    });
  };

  formElement.addEventListener('mousemove', handleMouseMove);
  formElement.addEventListener('mouseleave', handleMouseLeave);

  // –§—É–Ω–∫—Ü–∏—è –æ—á–∏—Å—Ç–∫–∏
  return () => {
    formElement.removeEventListener('mousemove', handleMouseMove);
    formElement.removeEventListener('mouseleave', handleMouseLeave);
  };
};

// –ê–Ω–∏–º–∞—Ü–∏—è —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —Å—Ç—Ä–∞–Ω–∏—Ü—ã
const animatePageElements = () => {
  if (!heading.value) return;

  // –ê–Ω–∏–º–∞—Ü–∏—è –∑–∞–≥–æ–ª–æ–≤–∫–∞
  anime({
    targets: heading.value,
    opacity: [0, 1],
    translateY: [20, 0],
    duration: 800,
    easing: 'easeOutExpo'
  });

  // –ê–Ω–∏–º–∞—Ü–∏—è —Ñ–æ—Ä–º—ã
  if (contactForm.value) {
    anime.timeline()
        .add({
          targets: [nameGroup.value, emailGroup.value, messageGroup.value, submitBtn.value],
          opacity: [0, 1],
          translateY: [20, 0],
          delay: anime.stagger(100),
          duration: 600,
          easing: 'easeOutCubic'
        });
  }

  // –ê–Ω–∏–º–∞—Ü–∏—è —Å–æ—Ü–∏–∞–ª—å–Ω—ã—Ö –∏–∫–æ–Ω–æ–∫
  if (socialSection.value) {
    anime({
      targets: socialSection.value,
      opacity: [0, 1],
      translateY: [20, 0],
      duration: 600,
      easing: 'easeOutCubic',
      delay: 300
    });

    anime({
      targets: '.social-icon',
      scale: [0, 1],
      opacity: [0, 1],
      delay: anime.stagger(60, {start: 500}),
      duration: 500,
      easing: 'easeOutBack'
    });

    anime({
      targets: '.contact-item',
      opacity: [0, 1],
      translateX: [-20, 0],
      delay: anime.stagger(100, {start: 700}),
      duration: 500,
      easing: 'easeOutCubic'
    });
  }
};

// –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –ø—Ä–∏ –º–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–∏–∏ –∫–æ–º–ø–æ–Ω–µ–Ω—Ç–∞
onMounted(() => {
  // –ê–Ω–∏–º–∏—Ä—É–µ–º —ç–ª–µ–º–µ–Ω—Ç—ã —Å—Ç—Ä–∞–Ω–∏—Ü—ã
  animatePageElements();

  // –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∏—Ä—É–µ–º 3D –≥–ª–æ–±—É—Å
  cleanupGlobe = initGlobe();

  // –ü—Ä–∏–º–µ–Ω—è–µ–º 3D —ç—Ñ—Ñ–µ–∫—Ç –∫ —Ñ–æ—Ä–º–µ
  const cleanupFormEffect = apply3DFormEffect();

  // –û—á–∏—Å—Ç–∫–∞ –ø—Ä–∏ —Ä–∞–∑–º–æ–Ω—Ç–∏—Ä–æ–≤–∞–Ω–∏–∏
  onBeforeUnmount(() => {
    if (cleanupGlobe) cleanupGlobe();
    if (cleanupFormEffect) cleanupFormEffect();
  });
});
</script>

