  // –ê–Ω–∏–º–∞—Ü–∏—è –≥–ª–æ–±—É—Å–∞
  const animate = () => {
    const animationId = requestAnimationFrame(animate);

    globe.rotation.y += 0.002;

    renderer.render(scene, camera);

    return animationId;
  };

